package Business.Models;

import Acquaintence.IChat;

public class Chat implements IChat {

    private int id;
    private String name;

    public Chat(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }
}
